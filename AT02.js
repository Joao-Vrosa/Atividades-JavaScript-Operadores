let numUm, numDois, multpica

numUm = Number(prompt("Digite um número: "))
numDois = Number(prompt("Digite um número: "))

multpica = numUm * numDois

console.log(`A multiplicação dos número ${numUm} e ${numDois} é ${multpica}`)

